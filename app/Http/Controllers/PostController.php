     * @return \Illuminate\Http\JsonResponse
     */
    public function store(Request $request)
    {
        $inputs = $request->all();
        $post = new Post();
        $post->name = $request->file('file_path')->store('card-posts');
        $post->card_id = $inputs['card_id'];
        $post->save();
        return response()->json(['status'=>200, 'message'=>'post added']);
    }

